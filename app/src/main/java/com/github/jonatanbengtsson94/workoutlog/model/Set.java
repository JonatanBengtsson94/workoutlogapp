package com.github.jonatanbengtsson94.workoutlog.model;

public class Set {
    private int reps;
    private float weight;

    public int getReps() {
        return reps;
    }

    public float getWeight() {
        return weight;
    }

    public void setWeight(float weight) {
        this.weight = weight;
    }

    public void setReps(int reps) {
        this.reps = reps;
    }
}
